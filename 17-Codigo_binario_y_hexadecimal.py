
# ! Código binario y hexadecimal:

# ? binario:
print(bin(int(12345)))

# * hexadecimal:
print(hex(int(12345)))

# ? binario:
numero = 0b101 #0b significa que el número se está ejecutando en binario

print(numero)

# * hexadecimal:
numero2 = 0x3039 #0x significa que el número se está ejecutando en hexadecimal

print(numero2)

# Ejemplo de uso para ambos sistemas:
numero = int(input("Ingrese un número entero: "))
print(bin(numero))
print(hex(numero))
    